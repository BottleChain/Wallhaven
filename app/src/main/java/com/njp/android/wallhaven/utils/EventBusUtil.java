package com.njp.android.wallhaven.utils;

/**
 * 设置事件类型
 */

public class EventBusUtil {

    /**
     * 滑动事件
     * 用于协调悬浮按钮和列表
     */
    public enum ScrollEvent {
        SCROLL_UP,
        SCROLL_DOWN,
        SCROLL_TO_TOP
    }

    /**
     * 换肤事件
     */
    public enum ChangeSkinEvent {
        SKIN_BLUE,
        SKIN_RED,
        SKIN_GREEN,
        SKIN_ORANGE,
        SKIN_PURPLE,
        SKIN_BROWN,
        SKIN_GRAY,
        SKIN_YELLOW,
        SKIN_CYAN
    }

}
